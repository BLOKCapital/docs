/**
 * SEO + GEO (Generative Engine Optimization) helpers.
 *
 * Centralizes everything search engines and answer engines (Google, Bing,
 * Perplexity, ChatGPT, Claude, Gemini) need to understand the site:
 *   - canonical URLs + hreflang alternates for the 3 locales,
 *   - JSON-LD structured-data builders (Organization, WebSite, TechArticle,
 *     BreadcrumbList, FAQPage, CollectionPage),
 *   - small content parsers (excerpt, FAQ extraction) that turn MDX into
 *     machine-readable snippets.
 *
 * Pure + framework-agnostic except for the tiny <JsonLd> render helper.
 */
import {
  LOCALES,
  SITE,
  ORG,
  OG_LOCALE,
  type Locale,
} from "@/lib/config";

/** Join a root-relative path onto the canonical site origin. */
export function absoluteUrl(path: string): string {
  return new URL(path, SITE.url).toString();
}

/**
 * The default social/answer-engine preview image, generated by
 * `app/opengraph-image.tsx`. Referenced explicitly because pages set their own
 * `openGraph`, and Next replaces (rather than deep-merges) that field, so the
 * file-convention image must be re-attached wherever `openGraph` is overridden.
 */
export const OG_IMAGE = {
  url: "/opengraph-image",
  width: 1200,
  height: 630,
  alt: `${SITE.brand}: ${SITE.tagline}`,
};

/**
 * hreflang alternates for a path that exists in every locale. `pathAfterLocale`
 * is the route *after* the `/<locale>` segment (no leading slash), e.g.
 * "concepts/diamond" or "" for a locale home.
 *
 * Returns absolute URLs keyed by language, plus `x-default` (the primary
 * locale) so engines know which version to show when no language matches.
 */
export function languageAlternates(
  pathAfterLocale: string,
): Record<string, string> {
  const suffix = pathAfterLocale ? `/${pathAfterLocale}` : "";
  const languages: Record<string, string> = {};
  for (const loc of LOCALES) {
    languages[loc] = absoluteUrl(`/${loc}${suffix}`);
  }
  languages["x-default"] = absoluteUrl(`/${LOCALES[0]}${suffix}`);
  return languages;
}

/** Canonical absolute URL for a locale-scoped path. */
export function canonicalFor(
  locale: Locale,
  pathAfterLocale: string,
): string {
  const suffix = pathAfterLocale ? `/${pathAfterLocale}` : "";
  return absoluteUrl(`/${locale}${suffix}`);
}

/**
 * `alternates` block ready to spread into a Next.js Metadata object:
 * canonical + per-language hreflang.
 */
export function alternatesFor(locale: Locale, pathAfterLocale: string) {
  return {
    canonical: canonicalFor(locale, pathAfterLocale),
    languages: languageAlternates(pathAfterLocale),
  };
}

