/**
 * Inline a JSON-LD payload as a <script type="application/ld+json">. Accepts a
 * single object or an array (emitted as a `@graph`-style list of scripts).
 * Safe in Server Components; content is build-time static.
 */
export function JsonLd({ data }: { data: Json | Json[] }) {
  const blocks = Array.isArray(data) ? data : [data];
  return (
    <>
      {blocks.map((block, i) => (
        <script
          key={i}
          type="application/ld+json"
          // JSON-LD is trusted, build-time content. `</script>` is escaped to
          // avoid breaking out of the tag.
          dangerouslySetInnerHTML={{
            __html: JSON.stringify(block).replace(/</g, "\\u003c"),
          }}
        />
      ))}
    </>
  );
}

/** Locale → Open Graph locale code, with alternates for the other languages. */
export function ogLocale(locale: Locale): { locale: string; alternateLocale: string[] } {
  return {
    locale: OG_LOCALE[locale],
    alternateLocale: LOCALES.filter((l) => l !== locale).map((l) => OG_LOCALE[l]),
  };
}
