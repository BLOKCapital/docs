/** Strip MDX/markdown syntax down to plain prose (mirrors the search indexer). */
export function toPlainText(body: string): string {
  return body
    .replace(/```[\s\S]*?```/g, " ")
    .replace(/`[^`]*`/g, " ")
    .replace(/^import .*$/gm, " ")
    .replace(/^#{1,6}\s+/gm, " ")
    .replace(/<[^>]+>/g, " ")
    .replace(/!\[[^\]]*\]\([^)]*\)/g, " ")
    .replace(/\[([^\]]+)\]\([^)]*\)/g, "$1")
    .replace(/:::[a-z]*/g, " ")
    .replace(/[*_~>|]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

/**
 * A ~155-char meta-description fallback derived from the first prose paragraph,
 * for the many docs whose frontmatter omits `description`. Good snippets are a
 * direct CTR lever and feed answer-engine summaries.
 */
export function excerpt(body: string, max = 155): string {
  const text = toPlainText(body);
  // Accumulate sentences, skipping leading section numbers ("1.") and other
  // ultra-short / letter-free fragments that make for useless snippets.
  const sentences = text.split(/(?<=[.!?])\s+/);
  let out = "";
  for (const s of sentences) {
    const clean = s.trim();
    if (!out && (clean.length < 12 || !/[a-zA-Z]{3,}/.test(clean))) continue;
    out = out ? `${out} ${clean}` : clean;
    if (out.length >= max) break;
  }
  if (!out) out = text;
  if (out.length <= max) return out;
  const clipped = out.slice(0, max);
  const lastSpace = clipped.lastIndexOf(" ");
  return `${(lastSpace > 60 ? clipped.slice(0, lastSpace) : clipped).trimEnd()}…`;
}

/**
 * Parse a Q&A-style MDX body into question/answer pairs. Each `###` (or `##`)
 * heading is treated as a question and the prose until the next heading as its
 * answer. Used to emit FAQPage schema from the FAQs doc.
 */
export function extractFaq(body: string): { question: string; answer: string }[] {
  const out: { question: string; answer: string }[] = [];
  const lines = body.split("\n");
  let current: { question: string; answer: string[] } | null = null;
  let inFence = false;

  const push = () => {
    if (current) {
      const answer = toPlainText(current.answer.join("\n"));
      if (current.question && answer) {
        out.push({ question: current.question, answer });
      }
    }
  };

  for (const line of lines) {
    if (/^\s*(```|~~~)/.test(line)) inFence = !inFence;
    const heading = !inFence && line.match(/^#{2,3}\s+(.+?)\s*#*\s*$/);
    if (heading) {
      push();
      current = { question: heading[1].replace(/[*_`]/g, "").trim(), answer: [] };
    } else if (current) {
      current.answer.push(line);
    }
  }
  push();
  return out;
}

