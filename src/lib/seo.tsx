type Json = Record<string, unknown>;

const PUBLISHER = {
  "@type": "Organization",
  name: ORG.name,
  url: SITE.siteUrl,
  logo: { "@type": "ImageObject", url: ORG.logo },
} as const;

/** Organization entity that anchors the brand in the knowledge graph. */
export function organizationLd(): Json {
  return {
    "@context": "https://schema.org",
    "@type": "Organization",
    "@id": `${SITE.siteUrl}/#organization`,
    name: ORG.name,
    legalName: ORG.legalName,
    url: SITE.siteUrl,
    logo: { "@type": "ImageObject", url: ORG.logo },
    description: SITE.description,
    sameAs: [...ORG.sameAs],
  };
}

/**
 * WebSite entity with a SearchAction so engines can surface the docs search box
 * (sitelinks search box). The query template points at the locale home with a
 * `?q=` param the client search reads.
 */
export function websiteLd(locale: Locale): Json {
  const home = canonicalFor(locale, "");
  return {
    "@context": "https://schema.org",
    "@type": "WebSite",
    "@id": `${SITE.url}/#website`,
    name: SITE.name,
    url: SITE.url,
    description: SITE.description,
    inLanguage: locale,
    publisher: { "@id": `${SITE.siteUrl}/#organization` },
    potentialAction: {
      "@type": "SearchAction",
      target: {
        "@type": "EntryPoint",
        urlTemplate: `${home}?q={search_term_string}`,
      },
      "query-input": "required name=search_term_string",
    },
  };
}

/** BreadcrumbList from an ordered trail of {name, url?} items. */
export function breadcrumbLd(
  items: { name: string; url?: string }[],
): Json {
  return {
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    itemListElement: items.map((item, i) => ({
      "@type": "ListItem",
      position: i + 1,
      name: item.name,
      ...(item.url ? { item: item.url } : {}),
    })),
  };
}

/** TechArticle for a documentation page. */
export function articleLd(opts: {
  title: string;
  description: string;
  url: string;
  locale: Locale;
  section: string;
}): Json {
  return {
    "@context": "https://schema.org",
    "@type": "TechArticle",
    headline: opts.title,
    description: opts.description,
    inLanguage: opts.locale,
    mainEntityOfPage: { "@type": "WebPage", "@id": opts.url },
    articleSection: opts.section,
    isPartOf: { "@id": `${SITE.url}/#website` },
    author: PUBLISHER,
    publisher: PUBLISHER,
  };
}

/** CollectionPage for a section landing / locale home. */
export function collectionPageLd(opts: {
  name: string;
  description: string;
  url: string;
  locale: Locale;
}): Json {
  return {
    "@context": "https://schema.org",
    "@type": "CollectionPage",
    name: opts.name,
    description: opts.description,
    url: opts.url,
    inLanguage: opts.locale,
    isPartOf: { "@id": `${SITE.url}/#website` },
  };
}

/** FAQPage from extracted Q&A pairs. */
export function faqLd(
  qa: { question: string; answer: string }[],
): Json {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: qa.map(({ question, answer }) => ({
      "@type": "Question",
      name: question,
      acceptedAnswer: { "@type": "Answer", text: answer },
    })),
  };
}

